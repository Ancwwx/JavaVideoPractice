public class TernaryOperatorDetail01{
public static void main(String[] args){
//1，先得到2的原码 --> 00000000 00000000 00000000 00000010 
//2，得到3的补码--> 00000000 00000000 00000000 00000011
//3，按位& --	> 00000000 00000000 00000000 00000010 （补码）
//4.补码换反码 00000000 00000000 00000000 00000010 （由于是正数，补码和原码相同，所以得到原码）
//5，换成十进制数字 2
	
	System.out.println(2&3);
//1，先得到-2的原码 10000000 00000000 00000000 00000010
//2，-2的反码 11111111 11111111 11111111 11111101
//	-2的补码 11111111 11111111 11111111 11111110　
//3，取反 00000000 00000000 00000000 00000001
//4，反码 00000000 00000000 00000000 00000001 （同上）
//5，原码 00000000 00000000 00000000 00000001 （同上）
//6，运算后的原码就是1
	
	System.out.println(~-2);   //~ 取反
//1,原码 00000000 00000000 00000000 00000010
//2,取反 11111111 11111111 11111111 11111101
//3,反码 11111111 11111111 11111111 11111100
//4,原码 10000000 00000000 00000000 00000011
//5,十进制数字是-3
	
	System.out.println(~2);
//注意取反符号也会变
	
	
	System.out.println(2|3);
	System.out.println(~-5);
	System.out.println(13&7);
	System.out.println(5|4);
	System.out.println(-3^3); //按位亦或
	
	
	
   }
}